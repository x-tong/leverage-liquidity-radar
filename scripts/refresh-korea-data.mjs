/**
 * Capture the KOFIA FreeSIS stock-market funding snapshot.
 *
 * KOFIA's public UI uses this JSON endpoint. We keep the untouched response in
 * the repository, name it with the source date plus a content hash, then build
 * the small typed snapshot consumed by the site. A malformed or changed
 * response stops the job before it can replace the previous valid snapshot.
 */
import { createHash } from 'node:crypto'
import { mkdir, writeFile } from 'node:fs/promises'

const ENDPOINT = 'https://freesis.kofia.or.kr/stockSubMain/STATSCUSUBMAIN01BO.do'
const SOURCE_URL = 'https://freesis.kofia.or.kr/stat/FreeSIS.do?parentDivId=MSIS10000000000000&serviceId=STATSCU0100000060'
const root = new URL('../', import.meta.url)

function toIsoDate(value) {
  if (typeof value !== 'string' || !/^\d{8}$/.test(value)) {
    throw new Error(`KOFIA supplied an invalid reference date: ${String(value)}`)
  }
  return `${value.slice(0, 4)}-${value.slice(4, 6)}-${value.slice(6, 8)}`
}

function numeric(value, label) {
  if (typeof value !== 'string' && typeof value !== 'number') {
    throw new Error(`KOFIA field ${label} was not numeric`)
  }
  const parsed = Number(value)
  if (!Number.isFinite(parsed) || parsed < 0) {
    throw new Error(`KOFIA field ${label} was not a non-negative number`)
  }
  return parsed
}

function rowFor(rows, category, label) {
  const matched = rows.filter((row) => row.TMPV1 === category && row.TMPV2 === label)
  if (matched.length !== 1) {
    throw new Error(`Expected exactly one KOFIA row for ${category} / ${label}, found ${matched.length}`)
  }
  return matched[0]
}

const response = await fetch(ENDPOINT, {
  method: 'POST',
  headers: {
    'content-type': 'application/json',
    referer: 'https://freesis.kofia.or.kr/ui/',
  },
  body: JSON.stringify({
    content: {
      userId: 'GUEST',
      serviceId: 'STATSCUSUBMAIN01',
      tmpV87: '1',
    },
  }),
})

if (!response.ok) throw new Error(`KOFIA request failed with HTTP ${response.status}`)

const raw = await response.text()
let payload
try {
  payload = JSON.parse(raw)
} catch {
  throw new Error('KOFIA did not return JSON')
}

if (payload?.success !== true || payload?.code !== 'SUCCESS' || !Array.isArray(payload?.dsResultList) || !payload?.dmTitle) {
  throw new Error(`KOFIA returned an unsuccessful response: ${payload?.message ?? 'unknown error'}`)
}

const rows = payload.dsResultList
const deposits = rowFor(rows, '증시자금', '투자자예탁금')
const marginCredit = rowFor(rows, '신용공여', '신용거래융자')
const collateralLoan = rowFor(rows, '신용공여', '예탁증권담보융자')
const totalCredit = rowFor(rows, '신용공여', '합계')
const unpaidReceivables = rowFor(rows, '증시자금', '위탁매매미수금')

const periods = Array.from({ length: 15 }, (_, index) => {
  const column = `TMPV${index + 6}`
  return {
    asOf: toIsoDate(payload.dmTitle[column]),
    investorDepositsMillions: numeric(deposits[column], `investor deposits ${column}`),
    marginCreditMillions: numeric(marginCredit[column], `margin credit ${column}`),
    totalCreditSupplyMillions: numeric(totalCredit[column], `total credit ${column}`),
  }
}).reverse()

if (new Set(periods.map((point) => point.asOf)).size !== periods.length) {
  throw new Error('KOFIA response contained duplicate dates')
}

const asOf = periods.at(-1)?.asOf
if (!asOf) throw new Error('KOFIA response contained no observations')

const snapshotHash = createHash('sha256').update(raw).digest('hex')
const archiveRelativePath = `data/raw/kr/${asOf}-${snapshotHash.slice(0, 12)}.json`
const archiveUrl = `https://raw.githubusercontent.com/x-tong/leverage-liquidity-radar/main/${archiveRelativePath}`
const archivePath = new URL(`../${archiveRelativePath}`, import.meta.url)
await mkdir(new URL('../data/raw/kr/', import.meta.url), { recursive: true })
await writeFile(archivePath, raw, { flag: 'wx' }).catch((error) => {
  if (error?.code !== 'EEXIST') throw error
})

const latest = periods.at(-1)
const snapshot = {
  refreshedAt: asOf,
  asOf,
  sourceUrl: SOURCE_URL,
  sourceHash: `sha256:${snapshotHash}`,
  archiveRelativePath,
  archiveUrl,
  investorDepositsMillions: latest.investorDepositsMillions,
  marginCreditMillions: latest.marginCreditMillions,
  collateralLoanMillions: numeric(collateralLoan.TMPV6, 'collateral loan TMPV6'),
  totalCreditSupplyMillions: latest.totalCreditSupplyMillions,
  unpaidReceivablesMillions: numeric(unpaidReceivables.TMPV6, 'unpaid receivables TMPV6'),
  history: periods,
}

const output = `// Generated by scripts/refresh-korea-data.mjs. Do not edit by hand.\nexport const latestKR = ${JSON.stringify(snapshot, null, 2)} as const\n`
await writeFile(new URL('../src/generated/latestKorea.ts', import.meta.url), output)
