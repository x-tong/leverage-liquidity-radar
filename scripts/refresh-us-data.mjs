import { writeFile } from 'node:fs/promises'

const FINRA_URL = 'https://www.finra.org/rules-guidance/key-topics/margin-accounts/margin-statistics'
const FRED = {
  highYieldOas: 'https://fred.stlouisfed.org/graph/fredgraph.csv?id=BAMLH0A0HYM2',
  fedAssets: 'https://fred.stlouisfed.org/graph/fredgraph.csv?id=WALCL',
}

function parseNumber(value) {
  return Number(value.replaceAll(',', '').trim())
}

function toIsoMonth(value) {
  const [month, year] = value.split('-')
  const months = { Jan: '01', Feb: '02', Mar: '03', Apr: '04', May: '05', Jun: '06', Jul: '07', Aug: '08', Sep: '09', Oct: '10', Nov: '11', Dec: '12' }
  return `20${year}-${months[month]}`
}

function latestCsvPoint(text) {
  const rows = text.trim().split(/\r?\n/).slice(1)
  for (let index = rows.length - 1; index >= 0; index -= 1) {
    const [date, raw] = rows[index].split(',')
    if (raw && raw !== '.') return { asOf: date, value: Number(raw) }
  }
  throw new Error('No numeric FRED observation found')
}

const [finraResponse, oasResponse, assetsResponse] = await Promise.all([
  fetch(FINRA_URL),
  fetch(FRED.highYieldOas),
  fetch(FRED.fedAssets),
])

if (!finraResponse.ok || !oasResponse.ok || !assetsResponse.ok) {
  throw new Error(`Source request failed: FINRA ${finraResponse.status}, OAS ${oasResponse.status}, assets ${assetsResponse.status}`)
}

const [finraPage, oasCsv, assetsCsv] = await Promise.all([
  finraResponse.text(),
  oasResponse.text(),
  assetsResponse.text(),
])

const tableMatch = finraPage.match(/<tbody><tr><td>([A-Z][a-z]{2}-\d{2})<\/td><td>([\d,]+)<\/td><td>([\d,]+)<\/td><td>([\d,]+)<\/td>/)
if (!tableMatch) throw new Error('Unable to parse latest FINRA margin statistics row')

const [, rawMonth, debit, freeCash, freeMargin] = tableMatch
const oas = latestCsvPoint(oasCsv)
const assets = latestCsvPoint(assetsCsv)
const output = `// Generated by scripts/refresh-us-data.mjs. Do not edit by hand.\nexport const latestUS = ${JSON.stringify({
  refreshedAt: new Date().toISOString().slice(0, 10),
  finra: {
    asOf: toIsoMonth(rawMonth),
    debitMillions: parseNumber(debit),
    freeCashMillions: parseNumber(freeCash),
    freeMarginMillions: parseNumber(freeMargin),
  },
  fred: {
    highYieldOas: oas,
    fedAssets: { asOf: assets.asOf, millions: assets.value },
  },
}, null, 2)} as const\n`

await writeFile(new URL('../src/generated/latestUs.ts', import.meta.url), output)
